public class Calculator {
    public int Sum(int x, int y){
        int sum=0;
        sum=x+y;
        return sum;
    }
    public int Sub(int x, int y){
        int sub=0;
        sub=x-y;
        return sub;
    }
    public int Mul(int x, int y){
        int mul=0;
        mul=x*y;
        return mul;
    }
    public int Div(int x, int y) {
        int div = 0;
        div = x / y;
        return div;
    }
}
